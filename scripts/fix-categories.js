async function main() {
  const dryRun = process.argv.includes("--dry-run");
  console.log(dryRun ? "[預覽模式]" : "[執行模式]");

  const data = await qaGet();
  if (!data) { console.log("無資料"); return; }

  let unitChanged = 0;
  let catChanged = 0;
  let total = 0;

  for (const [id, record] of Object.entries(data)) {
    if (record.deleted) continue;
    total++;

    let needsUpdate = false;
    const updates = { ...record };

    // 1. 同仁 → 社區駐點辦公室
    if (record.unit === "同仁") {
      updates.unit = "社區駐點辦公室";
      needsUpdate = true;
      unitChanged++;
      console.log(`[單位] ${id}: 同仁 → 社區駐點辦公室`);
    }

    // 2. 歸類細分類別
    const cat = record.category;
    if (cat && !STANDARD_CATS.includes(cat)) {
      const mapped = CAT_MAP[cat];
      if (mapped) {
        updates.category = mapped;
        needsUpdate = true;
        catChanged++;
        console.log(`[類別] ${id}: ${cat} → ${mapped}`);
      } else {
        // 未定義的對照 → 歸為「其他」
        updates.category = "其他";
        needsUpdate = true;
        catChanged++;
        console.log(`[類別] ${id}: ${cat} → 其他 (未定義)`);
      }
    }

    if (needsUpdate && !dryRun) {
      updates.updatedAt = new Date().toISOString();
      await qaPut(`/${id}`, updates);
    }
  }

  console.log(`\n========================================`);
  console.log(`共 ${total} 筆`);
  console.log(`單位更新：${unitChanged} 筆`);
  console.log(`類別更新：${catChanged} 筆`);
  if (dryRun) console.log(`移除 --dry-run 以執行實際更新`);
}

main().catch(e => { console.error(e); process.exit(1); });
